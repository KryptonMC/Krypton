class ItemFlatteningFix(outputSchema: Schema, changesType: Boolean) : DataFix(outputSchema, changesType) {

    override fun makeRule(): TypeRewriteRule {
        val itemType = inputSchema.getType(References.ITEM_STACK)
        val idFinder = fieldFinder("id", named(References.ITEM_NAME.typeName(), NamespacedSchema.NAMESPACED_STRING))
        val tagFinder = itemType.findField("tag")
        return fixTypeEverywhereTyped("ItemFlatteningFix", itemType) {
            val id = it.getOptional(idFinder)
            if (!id.isPresent) return@fixTypeEverywhereTyped it
            var typed = it
            val item = it[remainderFinder()]
            val damage = item["Damage"].asInt(0)
            val remappedKey = id.get().second.updateItem(damage)
            if (remappedKey != null) typed = it.set(idFinder, Pair.of(References.ITEM_NAME.typeName(), remappedKey))
            if (id.get().second in DAMAGE_IDS) {
                val typedTag = it.getOrCreateTyped(tagFinder)
                var tag = typedTag[remainderFinder()]
                tag = tag.set("Damage", tag.createInt(damage))
                typed = typed.set(tagFinder, typedTag.set(remainderFinder(), tag))
            }
            typed.set(remainderFinder(), item.remove("Damage"))
        }
    }

