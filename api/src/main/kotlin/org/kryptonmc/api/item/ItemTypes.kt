    @Suppress("MaxLineLength")
    private fun register(
        name: String,
        rarity: ItemRarity = ItemRarity.COMMON,
        maximumAmount: Int = 64,
        durability: Int = 0,
        isFireResistant: Boolean = false
    ): ItemType {
        val key = Key.key(name)
        return Registries.register(Registries.ITEM, key, ItemType(
            key,
            rarity,
            maximumAmount,
            durability,
            isFireResistant,
            Registries.BLOCK[key]?.translation
                ?: Component.translatable("item.${key.asString().replace(':', '.')}")
        ))
    }
